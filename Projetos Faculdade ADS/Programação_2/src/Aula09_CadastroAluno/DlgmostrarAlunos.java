     private void exibeInformacoes()  {
         ArrayList<Aluno> alunos = cv.retornarTodos();
         
         // Limpar JTable
         for (int i = 0; i < 200; i++) {
             tblAluno.setValueAt("", i, 0);
             tblAluno.setValueAt("", i, 1);
         }
         
         for (int i = 0; i < alunos.size() && alunos.get(i) != null; i++) {
             tblAluno.setValueAt(alunos.get(i).getNome(), i, 0);
             tblAluno.setValueAt(alunos.get(i).getEnddereco(), i, 1);
             tblAluno.setValueAt(alunos.get(i).getCidade(), i, 2);
             tblAluno.setValueAt(alunos.get(i).getEstado(), i, 3);
         }
     }
