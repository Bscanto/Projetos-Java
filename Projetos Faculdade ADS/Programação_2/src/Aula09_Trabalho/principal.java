    private void btnAdicionarPilhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarPilhaActionPerformed
        Pessoa pessoa = new Pessoa();

        if (txNome.getText().isEmpty() || txIdade.getText().isEmpty() || txSexo.getText().isEmpty() || txAltura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de adicionar uma pessoa!");
        } else {
        
        pessoa.setNome(txNome.getText());
        pessoa.setIdade(Integer.parseInt(txIdade.getText()));
        pessoa.setSexo(txSexo.getText());
        pessoa.setAltura(Double.parseDouble(txAltura.getText()));

        cp.salvar(pessoa); // adiciona a pessoa na pilha

        if (cp.salvar(pessoa)) {
            JOptionPane.showMessageDialog(null, "Pessoa Adicionada a Pilha!");
            txNome.setText("");
            txIdade.setText("");
            txSexo.setText("");
            txAltura.setText("");
            txNome.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao adicionada na Pilha!");
        }
    }//GEN-LAST:event_btnAdicionarPilhaActionPerformed
    }
        
    private void btnRetirarTopoPilhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarTopoPilhaActionPerformed
           if (cp.retornarTodos().isEmpty()) {
        JOptionPane.showMessageDialog(null, "A pilha está vazia!");
    } else {
        Pessoa p = cp.retornarTodos().pop(); // remove a pessoa no topo da pilha
        JOptionPane.showMessageDialog(null, "Pessoa " + p.getNome() + " retirada da pilha!");
    }
  
    }//GEN-LAST:event_btnRetirarTopoPilhaActionPerformed

  
