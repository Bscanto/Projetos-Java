class Pessoa {
    private String nome;
    private int idade;
    private String sexo;
    private double altura;

    public Pessoa(String nome, int idade, String sexo, double altura) {
        this.nome = nome;
        this.idade = idade;
        this.sexo = sexo;
        this.altura = altura;
    }

    public Pessoa() {
    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
    public String getSexo() {
        return sexo;
    }
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
    public double getAltura() {
        return altura;
    }
    public void setAltura(double altura) {
        this.altura = altura;
    }

    
}

class ControlaPessoa { // Professor O problema nessa classe é que quando sai da vusualização a pilha some
    private Stack<Pessoa> pessoas = new Stack<>();

    public boolean salvar(Pessoa p) {
        if (p != null) {
            pessoas.add(p);
            return true;
        } else {
            return false;
        }
    }

    public Stack<Pessoa> retornarTodos() {
        return pessoas;
    } 
}

